        bool ICollection<T>.IsReadOnly
        {
            get { return ((IList)_entities).IsReadOnly; }
        }

        #endregion

        #region Implementation of INotifyCollectionChanged

        public event NotifyCollectionChangedEventHandler CollectionChanged;
                 
        private void OnCollectionChanged(NotifyCollectionChangedAction action, object item, int index=-1)
        {
            if (CollectionChanged != null)
            {
                if (item != null)
                {
                    if (index > -1)
                        CollectionChanged(this, new NotifyCollectionChangedEventArgs(action, item, index));
                    else
                        CollectionChanged(this, new NotifyCollectionChangedEventArgs(action, item));
                }
                else
                    CollectionChanged(this, new NotifyCollectionChangedEventArgs(action));
            }
        }
        #endregion
    }
