    public class EntitySetProxy<T> : ICollection<T>, ICollection, IEnumerable<T>, IEnumerable, IList, IList<T>, INotifyCollectionChanged where T : class
    {
        private EntitySet<T> _entities;


        public EntitySetProxy(EntitySet<T> wrappedEntities)
        {
            Contract.Requires(wrappedEntities != null);
            _entities = wrappedEntities;
        }

        #region Implementation of IEnumerable

        /// <summary>
